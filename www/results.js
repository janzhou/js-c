function draw(id){
	if(!draw_do) return;
	if(id <= 7 && id >= 0) id++;
	else id = 0;
	setTimeout("draw("+id+")", interval);
    var plot1 = $.jqplot('chart-'+id, results[id], {
        title: 'Machine '+id,
        axesDefaults: {
			labelRenderer: $.jqplot.CanvasAxisLabelRenderer
		}
    });
}

function startdraw(){
	if(draw_do) return;
	draw_do = 1;
	draw();
}

function stopdraw(){
	$('#result-start').unbind();
	draw_do = 0;
	setTimeout('$("#result-start").click(startdraw);', interval);
}

$(document).ready(function(){
	$('#result-start').click(startdraw);
	$('#result-stop').click(stopdraw);
});
