function draw(){
	if(!draw_do) return;
	
	setTimeout('draw()', interval);
	
	$('#chart').empty();

    var plot1 = $.jqplot('chart', results[$("#draw-machine").val()][$("#draw-disk").val()], {
        title: 'Machine '+$("#draw-machine").val()+' Disk '+$("#draw-disk").val(),
        axesDefaults: {
			labelRenderer: $.jqplot.CanvasAxisLabelRenderer
		}
    });
}

function startdraw(){
	if(draw_do) return;
	draw_do = 1;
	draw();
}

function stopdraw(){
	$('#result-start').unbind();
	draw_do = 0;
	setTimeout('$("#result-start").click(startdraw);', interval);
}

$(document).ready(function(){
	$('#result-start').click(startdraw);
	$('#result-stop').click(stopdraw);
	startdraw();
});
